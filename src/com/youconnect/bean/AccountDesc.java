package com.youconnect.bean;

public class AccountDesc {
	private int descId;
	private String firstName;
	private String lastName;
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	private String emailId;
	private String friendId;
	private int groupId;
	private int documentId;
	private int forumId;
	private int selfFlag;
	private int friendsFlag;
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getFriendId() {
		return friendId;
	}
	public void setFriendId(String friendId) {
		this.friendId = friendId;
	}
	public int getGroupId() {
		return groupId;
	}
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
	public int getDocumentId() {
		return documentId;
	}
	public void setDocumentId(int documentId) {
		this.documentId = documentId;
	}
	public int getForumId() {
		return forumId;
	}
	public void setForumId(int forumId) {
		this.forumId = forumId;
	}
	public int getSelfFlag() {
		return selfFlag;
	}
	public void setSelfFlag(int selfFlag) {
		this.selfFlag = selfFlag;
	}
	public int getFriendsFlag() {
		return friendsFlag;
	}
	public void setFriendsFlag(int friendsFlag) {
		this.friendsFlag = friendsFlag;
	}
	public int getDescId() {
		return descId;
	}
	public void setDescId(int descId) {
		this.descId = descId;
	}
	

}
